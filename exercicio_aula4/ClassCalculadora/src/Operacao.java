public class Operacao {
    public float soma(float x, float y){
        float resultado = x + y;

        return resultado;
    }

    public float subtracao(float x, float y){
        float resultado = x - y;

        return resultado;
    }

    public float multiplicacao(float x, float y){
        float resultado = x * y;

        return resultado;
    }

    public float divisao(float x, float y){
        float resultado = x / y;

        return resultado;
    }
}
